package jerik.util;

class Bit {
	final int value;
	final char alphaValue;
	final String name;
	final String description;

	public Bit(int value, char alphaValue, String name, String description) {
		this.value = value;
		this.alphaValue = alphaValue;
		this.name = name;
		this.description = description;
	}
}
